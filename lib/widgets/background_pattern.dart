  Widget _buildPatternIcon(
    BoxConstraints constraints,
    IconData icon,
    Color color,
    double opacity,
    double size,
    double? topPos,
    double? leftPos,
    double rotationDegrees, {
    double? right,
    double? bottom,
  }) {
