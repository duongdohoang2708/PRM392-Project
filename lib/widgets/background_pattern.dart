      right: right != null ? constraints.maxWidth * right : null,
      bottom: bottom != null ? constraints.maxHeight * bottom : null,
      child: Transform.rotate(
        angle: rotationDegrees * (math.pi / 180),
        child: Icon(
          icon,
          color: color.withAlpha((255 * opacity).round()),
          size: size,
        ),
      ),
    );
